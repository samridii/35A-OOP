package Task.week2.week3;

public class PrefixPostfix {
    public static void main(String[] args) {
        /*Prefix operator */
    int prefixValue=10;
    System.out.println("Prefix value is : "+ ++prefixValue);
    System.out.println("It remains the same in next line"+prefixValue);

    /*PostFix operator */
    int postFixValue=20;
    System.out.println("Postfix value is : "+ postFixValue++);
    System.out.println("It value changes in line"+postFixValue);

    }

    
}
